using Test
using SLMTools.PhaseGenerators

@testset "PhaseGenerators tests" begin
    arr0 = makeRampedParabola((10, 10), 5, (5, 5), 1, (1, 0))
    arr = convert(Array{Float64}, arr0)

    @testset "makeRampedParabola tests" begin
        @test arr[5, :] ≈ [0.6392156862745098, 0.3607843137254902, 0.1607843137254902, 0.0392156862745098, 0.0, 0.0392156862745098, 0.1607843137254902, 0.3607843137254902, 0.6392156862745098, 0.0]
        @test arr[:, 5] ≈ [0.8392156862745098, 0.7607843137254902, 0.7607843137254902, 0.8392156862745098, 0.0, 0.23921568627450981, 0.5607843137254902, 0.9607843137254902, 0.4392156862745098, 0.0]
    end
end